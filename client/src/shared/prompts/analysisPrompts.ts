import type { AnalysisType, ChatMessage } from '../types';

export interface BuildAnalysisMessagesInput {
  fileContent: string;
  analysisType: AnalysisType;
}

export function buildInvalidBidAndRejectionItemsPrompt() {
  return `任务：提取并分析招标文件中的“无效投标”和“废标项”。

概念边界：
1. “无效投标”指投标人、投标文件、签章密封、递交时间、报价、保证金、资格条件、实质性响应等原因导致投标被认定为无效、否决、不予受理或按无效响应处理的情形。
2. “废标项”指可能导致项目废标、采购失败、重新招标、终止评审、有效投标人不足或实质性响应不足的条款或风险项。
3. 原文使用“否决投标”“投标无效”“不予受理”“无效响应”“重大偏差”“实质性偏离”“废标情形”等同义表达时，也要按上述边界归类。

输出要求：
1. 必须明确区分“无效投标”和“废标项”。
2. “原文中明确提到的”只能提取招标文件原文中明确出现或同义表达的内容，尽量保留原文关键句；如果没有提及，写“- 原文未提及”。
3. “此类标书还可能涉及的”只补充原文未明确提及、但结合本招标文件类型和招投标经验判断非常重要的高风险遗漏项。
4. 不要罗列所有常见可能项，不要输出泛泛的通用清单；每个小节最多输出 3-5 条。
5. 如果没有明显需要补充的关键项，写“- 暂未发现必须补充的高风险项”。
6. 经验补充项每条前缀使用“重点补充：”，并用一句话说明为什么需要关注。
7. 不要使用表格，使用 Markdown 列表。
8. 仅输出下方格式，不要输出解释、过程或额外段落。
9. 不要输出三重引号、代码块标记或其他格式包裹符。

输出格式：
# 原文中明确提到的

## 无效投标
- ...

## 废标项
- ...

# 此类标书还可能涉及的

## 无效投标
- 重点补充：...

## 废标项
- 重点补充：...`;
}

export function buildAnalysisMessages({ fileContent, analysisType }: BuildAnalysisMessagesInput): ChatMessage[] {
  const systemPrompt = `你是一名专业的招标文件分析助手。请严格基于用户提供的招标文件原文完成分析任务。

通用要求：
1. 保持信息全面、准确，尽量使用原文内容，不要自行编造。
2. 只输出最终分析结果，不要输出过程、提示语或客套话。
3. 如果原文不足以支持某项结论，明确写“原文未提及”。`;

  const taskPrompt = analysisType === 'overview'
    ? `任务：提取并总结项目概述信息。

请重点关注：
1. 项目名称和基本信息
2. 项目背景和目的
3. 项目规模和预算
4. 项目时间安排
5. 项目实施内容
6. 主要技术特点
7. 其他关键要求

要求：只关注与项目实施有关的内容，不提取商务信息。直接返回整理后的项目概述。`
    : `任务：提取技术评分要求。

重点识别“技术评分”“评标方法”“评分标准”“技术参数”“技术要求”“技术方案”“技术部分”等相关章节，不要提取商务、价格、资质等无关条目。

每一项尽量按以下格式输出：
【评分项名称】：原文描述
【权重/分值】：具体分值或占比
【评分标准】：详细规则
【数据来源】：章节、条款或表格位置

若信息缺失，标注“未提及”。直接返回提取结果。`;

  return [
    { role: 'system', content: systemPrompt },
    { role: 'user', content: `以下是完整招标文件全文，请先完整阅读，并仅基于原文完成后续任务：\n\n${fileContent}` },
    { role: 'user', content: taskPrompt },
  ];
}
